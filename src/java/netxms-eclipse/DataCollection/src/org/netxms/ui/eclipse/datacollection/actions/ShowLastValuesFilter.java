package org.netxms.ui.eclipse.datacollection.actions;

import org.eclipse.jface.action.IAction;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.jface.viewers.IStructuredSelection;
import org.eclipse.ui.IViewActionDelegate;
import org.eclipse.ui.IViewPart;
import org.netxms.ui.eclipse.datacollection.objecttabs.LastValues;

/**
 * Show last values filter for "Last Values" tab
 */
public class ShowLastValuesFilter implements IViewActionDelegate
{
	private LastValues tab;
	
	/* (non-Javadoc)
	 * @see org.eclipse.ui.IViewActionDelegate#init(org.eclipse.ui.IViewPart)
	 */
	@Override
	public void init(IViewPart view)
	{
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.IActionDelegate#run(org.eclipse.jface.action.IAction)
	 */
	@Override
	public void run(IAction action)
	{
		if (tab != null)
			tab.setFilterEnabled(action.isChecked());
	}

	/* (non-Javadoc)
	 * @see org.eclipse.ui.IActionDelegate#selectionChanged(org.eclipse.jface.action.IAction, org.eclipse.jface.viewers.ISelection)
	 */
	@Override
	public void selectionChanged(IAction action, ISelection selection)
	{
		Object element = ((IStructuredSelection)selection).getFirstElement();
		if ((element != null) && (element instanceof LastValues))
		{
			tab = (LastValues)element;
			action.setEnabled(true);
		}
		else
		{
			tab = null;
			action.setEnabled(false);
		}
	}
}

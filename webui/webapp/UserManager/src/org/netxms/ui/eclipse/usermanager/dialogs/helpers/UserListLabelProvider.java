package org.netxms.ui.eclipse.usermanager.dialogs.helpers;

import org.eclipse.jface.viewers.LabelProvider;
import org.eclipse.swt.graphics.Image;
import org.eclipse.ui.model.WorkbenchLabelProvider;

/**
 * Label provider for user list in user selection dialog
 */
public class UserListLabelProvider extends LabelProvider
{
   WorkbenchLabelProvider wbLabelProvider = new WorkbenchLabelProvider();
   
   /**
    * @see org.eclipse.jface.viewers.LabelProvider#getImage(java.lang.Object)
    */
   @Override
   public Image getImage(Object element)
   {
      return wbLabelProvider.getImage(element);
   }

   /**
    * @see org.eclipse.jface.viewers.LabelProvider#getText(java.lang.Object)
    */
   @Override
   public String getText(Object element)
   {
      return (element instanceof String) ? (String)element : wbLabelProvider.getText(element);
   }

   /**
    * @see org.eclipse.jface.viewers.BaseLabelProvider#dispose()
    */
   @Override
   public void dispose()
   {
      wbLabelProvider.dispose();
      super.dispose();
   }
}
